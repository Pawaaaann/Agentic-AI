from langchain.tools import tool 
import requests
from bs4 import BeautifulSoup
from tavily import TavilyClient
import os 
from dotenv import load_dotenv
from rich import print
load_dotenv()

tavily = TavilyClient(api_key=os.getenv("TAVILY_API_KEY"))

TAVILY_MAX_RESULTS = int(os.getenv("TAVILY_MAX_RESULTS", "2"))
SNIPPET_MAX_CHARS = int(os.getenv("SNIPPET_MAX_CHARS", "120"))
MAX_SCRAPE_CHARS = int(os.getenv("MAX_SCRAPE_CHARS", "500"))

@tool
def web_search(query : str) -> str:
    """Search the web for recent and reliable information on a topic . Returns Titles , URLs and snippets."""
    results = tavily.search(query=query, max_results=TAVILY_MAX_RESULTS)

    out = []

    for r in results['results']:
        out.append(
            f"Title: {r['title']}\nURL: {r['url']}\nSnippet: {r['content'][:SNIPPET_MAX_CHARS]}\n"
        )
    
    return "\n----\n".join(out)

@tool
def scrape_url(url: str) -> str:
    """Scrape and return clean text content from a given URL for deeper reading."""
    try:
        resp = requests.get(url, timeout=8, headers={"User-Agent": "Mozilla/5.0"})
        soup = BeautifulSoup(resp.text, "html.parser")
        for tag in soup(["script", "style", "nav", "footer"]):
            tag.decompose()
        return soup.get_text(separator=" ", strip=True)[:MAX_SCRAPE_CHARS]
    except Exception as e:
        return f"Could not scrape URL: {str(e)}"

